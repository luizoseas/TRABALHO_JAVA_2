    public static void atualizarTabela(){
        Tabela.getDataVector().removeAllElements();
        for (Produto prod : produtoDAO.getProdutos()) {
            String data[] = {prod.getIdproduto()+"",prod.getProdNome(),prod.getProdValidade().toString()};
            Tabela.addRow(data);   
        }
    }

